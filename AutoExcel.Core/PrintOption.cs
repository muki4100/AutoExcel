using System;
using System.Collections.Generic;
using System.Text;

namespace AutoExcel.Core
{
    public class PrintOption
    {
        public AutoExcel.Core.ExcelBinder.XlPageOrientation pageShape = ExcelBinder.XlPageOrientation.xlPortrait;
        public Margins margin;
        public AutoExcel.Core.ExcelBinder.xlPaperSize papersize = ExcelBinder.xlPaperSize.xlPaperA4;
        public int zoompercent = 100;

    }



    public class Margins
    {
        double left;
        double top;
        double right;
        double bottom;

        public Margins(double l, double t, double r, double b)
        {
            left = l;
            top = t;
            right = r;
            bottom = b;
        }
    }
}
